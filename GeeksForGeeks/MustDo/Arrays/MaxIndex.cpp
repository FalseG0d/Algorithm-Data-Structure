class Solution{
    public:
        
    // A[]: input array
    // N: size of array
    // Function to find the maximum index difference.
    int maxIndexDiff(int A[], int N) 
    { 
        // Your code here
        int low = 0;
        int high= N - 1;
        
        int res = INT_MIN;
        
        while(low <= high){
            if(A[high] >= A[low]){
                res = max(res, high - low);
                low++;
                high = N - 1;
            }
            else high--;
        }
        
        return res;
    }
};

