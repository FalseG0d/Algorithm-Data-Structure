class Solution{
    public:
        //Function to merge the arrays.
        void merge(long long arr1[], long long arr2[], int n, int m) 
        { 
            // code here 
            int itr1 = n-1;
            int itr2 = 0;
            
            while(arr1[itr1] > arr2[itr2] && itr1 >= 0 && itr2 < m) {
                int tmp = arr1[itr1];
                arr1[itr1] = arr2[itr2];
                arr2[itr2] = tmp;
                
                itr1--;
                itr2++;
            }
            
            sort(arr1, arr1 + n);
            sort(arr2, arr2 + m);
        } 
};

